                            <h4 class="panel-title" ">
                                <a data-toggle="collapse" data-parent="#accordion" href="#collapse1">
                                    Grade Point Average (GPA)</a>
                            </h4>
                        </div>
                        <div id="collapse1" class="panel-collapse collapse in">
                            <div class="panel-body">The Grade Point Average provides a continuous scale for grades earned. Two types of averages are computed for the grades obtained by a student, the semester Grade Point Average (SGPA) and the Cumulative Grade Point Average (CGPA). SGPA gives the GPA of courses completed in a semester, whereas CGPA gives teh GPA of all courses completed thus far by a student </div>
                        </div>
                    </div>



                    <div class="panel panel-default">
                        <div class="panel-heading">
                            <h4 class="panel-title">
                                <a data-toggle="collapse" data-parent="#accordion" href="#collapse3">
                                    GPA Calculation</a>
                            </h4>
                        </div>
                        <div id="collapse3" class="panel-collapse collapse">
                            <div class="panel-body"> SGPA is calculated as follows:<br>
                                SGPA = (P1*C1 + P2*C2 + P3*C3 + P4*C4)/C<br><br>

                                Where P1, P2, P3 and P4 are the grade points earned in particular courses, while C1, C2, C3, C4 are the credit hours of respective course studied in
                                the semester and C is the number of total credits attempted in the semester, that is, C= C1+#C2+C3+C4.
<br><br>
                                 CGPA is calculated as the weighted average for all semesters cumulatively using grade points of all the courses taken by a student.
<br><br>
                                 In CGPA calculation for such courses that have been repeated, only the most recent grade points are used, even if they are less than the earlier
                                attempts.
<br><br>
                                 “W" will be awarded for withdrawn courses. It will appear on the transcript but will not affect the CGPA.
<br><br>
                                “I” is atemporary grade given for incomplete work. If it is not changed to an earned grade, it shall automatically convert to F grade after a fixed
                                time communicated by the competent authority.</div>
                        </div>
