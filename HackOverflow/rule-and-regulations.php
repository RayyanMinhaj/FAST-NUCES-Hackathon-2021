                    1.24 When a student is readmitted with credit exemptions from a previous admission, the
                    maximum duration allowed to complete the degree will be counted from the date of first
                    admission.<br>
                    1.25 Each bachelor’s degree program is of minimum of 130 credit hours. A program may
                    have more credit hours as approved by the university.<br>
