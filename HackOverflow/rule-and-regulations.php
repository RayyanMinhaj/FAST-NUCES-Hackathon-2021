                    illness, etc.<br>
                    1.22 Failure to meet attendance requirements in a course will render the student ineligible to
                    appear in the final examination of the concerned course.</p>


                <h3>Bachelor’s Degree Requirements</h3>
                <p style="color: #3b5998">1.23 The minimum duration of a bachelor’s degree is four academic years (8 regular
